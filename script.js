function extractYouTubeId(url) {
  if (!url) return '';
  const m = url.match(/(?:youtu\.be\/|youtube\.com\/(?:watch\?.*v=|embed\/|shorts\/))([A-Za-z0-9_-]{11})/);
  if (m) return m[1];
  if (/^[A-Za-z0-9_-]{11}$/.test(url.trim())) return url.trim();
  return '';
}

function guessMediaType(src) {
  if (/\.(mp4|webm|mov|avi)$/i.test(src)) return 'video';
  if (/\.(mp3|wav|ogg|flac|aac)$/i.test(src)) return 'audio';
  if (/\.pdf$/i.test(src)) return 'pdf';
  return 'image';
}

function mimeToMediaType(mime) {
  if (mime.startsWith('video/')) return 'video';
  if (mime.startsWith('audio/')) return 'audio';
  if (mime === 'application/pdf') return 'pdf';
  return 'image';
}

function buildMediaItems(work) {
  const items = [];
  const ytId = extractYouTubeId(work.youtube || '');
  if (ytId) items.push({ type: 'youtube', src: ytId, localThumb: getThumb(work) || work.src || '', useCustomThumb: !!work.ytThumbCustom });
  const ytId2 = extractYouTubeId(work.youtube2 || '');
  if (ytId2) items.push({ type: 'youtube', src: ytId2, localThumb: '', useCustomThumb: false });
  if (Array.isArray(work.media) && work.media.length) {
    items.push(...work.media.filter((m) => m.src));
  } else {
    const src = work.type === 'image' ? (getThumb(work) || work.src) : work.src;
    if (src) items.push({ type: work.type, src });
  }
  return items;
}

function renderMediaItem(item, title) {
  if (item.type === 'youtube') {
    const ytMax = `https://img.youtube.com/vi/${escapeHtml(item.src)}/maxresdefault.jpg`;
    const ytHq = `https://img.youtube.com/vi/${escapeHtml(item.src)}/hqdefault.jpg`;
    const local = item.localThumb ? escapeHtml(item.localThumb) : '';
    const href = `https://youtu.be/${escapeHtml(item.src)}`;
    const imgSrc = (item.useCustomThumb && local) ? local : ytMax;
    const onerror = (item.useCustomThumb && local) ? '' : `onerror="this.onerror=null;this.src='${local || ytHq}'"`;
    return `<a class="yt-thumb" href="${href}" target="_blank" rel="noreferrer" aria-label="Watch on YouTube"><img src="${imgSrc}" ${onerror} alt="${escapeHtml(title)}" /><span class="yt-play" aria-hidden="true">&#9654;</span></a>`;
  }
  if (item.type === 'pdf') {
    return `<iframe src="${escapeHtml(item.src)}" title="${escapeHtml(title)} PDF preview"></iframe>`;
  }
  if (item.type === 'video') {
    return `<video src="${escapeHtml(item.src)}" controls playsinline></video>`;
  }
  if (item.type === 'audio') {
    return `<div class="audio-stage"><audio src="${escapeHtml(item.src)}" controls></audio></div>`;
  }
  return `<img src="${escapeHtml(item.src)}" alt="${escapeHtml(title)}" />`;
}


function constrainStageImage() {
  const stage = detailMedia.querySelector('.carousel-stage');
  if (!stage) return;
  const img = stage.querySelector(':scope > img');
  if (img && stage.clientHeight > 0) {
    img.style.maxHeight = stage.clientHeight + 'px';
    img.style.maxWidth = stage.clientWidth + 'px';
    img.style.width = 'auto';
    img.style.height = 'auto';
  }
}

function updateCarousel() {
  const stage = detailMedia.querySelector('.carousel-stage');
  const counter = detailMedia.querySelector('.carousel-counter');
  if (!stage) return;
  stage.innerHTML = renderMediaItem(mediaItems[mediaIndex], detailTitle.textContent);
  constrainStageImage();
  if (counter) counter.textContent = `${mediaIndex + 1} / ${mediaItems.length}`;
}

function renderDetailMedia(work) {
  mediaItems = buildMediaItems(work);
  mediaIndex = 0;

  if (!mediaItems.length) {
    return `<p class="empty-media">이미지 준비중</p>`;
  }

  if (mediaItems.length === 1) {
    return renderMediaItem(mediaItems[0], work.title);
  }

  return `
    <div class="media-carousel">
      <div class="carousel-stage">${renderMediaItem(mediaItems[0], work.title)}</div>
      <div class="carousel-nav">
        <button class="carousel-btn" type="button" data-dir="-1" aria-label="이전">&#8249;</button>
        <span class="carousel-counter">1 / ${mediaItems.length}</span>
        <button class="carousel-btn" type="button" data-dir="1" aria-label="다음">&#8250;</button>
      </div>
    </div>
  `;
}

