function renderFilters() {
  const genres = getGenres();
  if (!genres.includes(activeGenre)) activeGenre = 'All';
  filters.innerHTML = genres
    .map(
      (genre) => `
        <button class="filter-button" type="button" data-genre="${escapeHtml(genre)}" aria-pressed="${genre === activeGenre}">
          ${escapeHtml(genre)}
        </button>
      `,
    )
    .join('');
}

function getFilteredWorks() {
  const query = search.value.trim().toLowerCase();
  return works.filter((work) => {
    const matchesGenre = activeGenre === 'All' || work.genre.toLowerCase().includes(activeGenre.toLowerCase());
    const matchesSearch = [work.title, work.genre, work.year, work.status].join(' ').toLowerCase().includes(query);
    return matchesGenre && matchesSearch;
  });
}

