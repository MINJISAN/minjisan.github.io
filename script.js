function loadWorks() {
  try {
    const saved = JSON.parse(localStorage.getItem(storageKey));
    return Array.isArray(saved) && saved.length ? saved : structuredClone(defaultWorks);
  } catch {
    return structuredClone(defaultWorks);
  }
}

function saveWorks() {
  try {
    localStorage.setItem(storageKey, JSON.stringify(works));
  } catch {
    // base64 데이터 제거 후 재시도
    const stripped = works.map((w) => ({
      ...w,
      thumb: w.thumb?.startsWith('data:') ? '' : (w.thumb || ''),
      src: w.src?.startsWith('data:') ? '' : (w.src || ''),
      media: w.media?.map((m) => ({
        ...m,
        src: m.src?.startsWith('data:') ? '' : m.src,
      })),
    }));
    try {
      localStorage.setItem(storageKey, JSON.stringify(stripped));
    } catch {
      // 용량 초과 시 Save to script.js 버튼으로 직접 저장
    }
  }
}

function getGenres() {
  return [...new Set(works.map((work) => work.genre.split('/')[0].trim()).filter(Boolean))];
}

function renderAll() {
  renderFilters();
  renderWorks();
  if (isEditorPage) renderEditorList();
}

function renderFilters() {
  const genres = getGenres();
  if (!genres.includes(activeGenre)) activeGenre = 'All';
  filters.innerHTML = genres
    .map(
      (genre) => `
        <button class="filter-button" type="button" data-genre="${escapeHtml(genre)}" aria-pressed="${genre === activeGenre}">
          ${escapeHtml(genre)}
        </button>
      `,
    )
    .join('');
}

function getFilteredWorks() {
  const query = search.value.trim().toLowerCase();
  return works.filter((work) => {
    const matchesGenre = activeGenre === 'All' || work.genre.toLowerCase().includes(activeGenre.toLowerCase());
    const matchesSearch = [work.title, work.genre, work.year, work.status].join(' ').toLowerCase().includes(query);
    return matchesGenre && matchesSearch;
  });
}

function renderWorks() {
  const visibleWorks = getFilteredWorks();
  if (resultCount) resultCount.textContent = String(visibleWorks.length);
  if (workCount) workCount.textContent = String(works.length);
  grid.innerHTML = visibleWorks.map(renderCard).join('');
}

function renderCard(work, index) {
  const thumbSrc = getThumb(work);
  const thumb = thumbSrc ? `<img src="${thumbSrc}" alt="${escapeHtml(work.title)} thumbnail" loading="lazy" />` : '';

  return `
    <button class="work-card" type="button" data-id="${escapeHtml(work.id)}">
      <span class="thumb">
        ${thumb}
      </span>
      <span class="card-copy">
        <span class="card-topline">
          <span>${String(index + 1).padStart(2, '0')}</span>
          <span>${escapeHtml(work.year || 'TBD')}</span>
        </span>
        <span class="card-title">${escapeHtml(work.title)}</span>
        <span class="card-bottom">
          <span class="card-genre">${escapeHtml(work.genre)}</span>
          <span class="card-role">${escapeHtml(work.role || '')}</span>
        </span>
      </span>
    </button>
  `;
}

