function renderDetailMedia(work) {
  mediaItems = buildMediaItems(work);
  mediaIndex = 0;

  if (!mediaItems.length) {
    return `<p class="empty-media">이미지 준비중</p>`;
  }

  if (mediaItems.length === 1) {
    return renderMediaItem(mediaItems[0], work.title);
  }

  return `
    <div class="media-carousel">
      <div class="carousel-stage">${renderMediaItem(mediaItems[0], work.title)}</div>
      <div class="carousel-nav">
        <button class="carousel-btn" type="button" data-dir="-1" aria-label="이전">&#8249;</button>
        <span class="carousel-counter">1 / ${mediaItems.length}</span>
        <button class="carousel-btn" type="button" data-dir="1" aria-label="다음">&#8250;</button>
      </div>
    </div>
  `;
}

function fillForm(work) {
  if (!isEditorPage) return;
  const item = work || {};
  fields.id.value = item.id || '';
  fields.title.value = item.title || '';
  fields.genre.value = item.genre || '';
  fields.year.value = item.year || '';
  fields.type.value = item.type || 'image';
  fields.status.value = item.status || '';
  fields.role.value = item.role || '';
  fields.form.value = item.form || '';
  fields.venue.value = item.venue || '';
  fields.date.value = item.date || '';
  fields.note.value = item.note || '';
  fields.credits.value = item.credits || '';
  fields.youtube.value = item.youtube || '';
  const pathItems = Array.isArray(item.media)
    ? item.media.filter((m) => m.src && !m.src.startsWith('data:')).map((m) => m.src)
    : item.src && !item.src.startsWith('data:') ? [item.src] : [];
  fields.mediaPathsArea.value = pathItems.join('\n');
  fields.description.value = item.description || '';
  fields.thumbFile.value = '';
  fields.srcFile.value = '';
  editorMode.textContent = item.id ? 'Edit Work' : 'New Work';
  selectedWorkId = item.id || '';
}

async function getFormWork() {
  const existing = works.find((work) => work.id === fields.id.value);
  const title = fields.title.value.trim();
  const thumbData = await readFileAsDataUrl(fields.thumbFile.files[0]);

  const fileList = Array.from(fields.srcFile.files);
  const fileDatas = await Promise.all(fileList.map((f) => readFileAsDataUrl(f)));
  const pathLines = fields.mediaPathsArea.value.trim().split('\n').map((s) => s.trim()).filter(Boolean);

  const mediaArr = [
    ...fileDatas.filter(Boolean).map((d, i) => ({ type: mimeToMediaType(fileList[i].type), src: d })),
    ...pathLines.map((p) => ({ type: guessMediaType(p), src: p })),
  ];

  // 파일도 없고 경로도 없으면 미디어를 완전히 지운 것으로 처리
  const src = mediaArr[0]?.src || '';
  const type = mediaArr[0]?.type || fields.type.value;

  return {
    ...(existing || {}),
    id: existing?.id || makeId(title),
    title,
    genre: fields.genre.value.trim(),
    year: fields.year.value.trim() || 'TBD',
    type,
    status: fields.status.value.trim() || 'Draft',
    role: fields.role.value.trim(),
    form: fields.form.value.trim(),
    venue: fields.venue.value.trim(),
    date: fields.date.value.trim(),
    note: fields.note.value.trim(),
    credits: fields.credits.value.trim(),
    youtube: fields.youtube.value.trim(),
    thumb: thumbData || existing?.thumb || '',
    src,
    media: mediaArr.length ? mediaArr : undefined,
    description: fields.description.value.trim(),
  };
}

async function saveForm(event) {
  event.preventDefault();
  const nextWork = await getFormWork();
  const index = works.findIndex((work) => work.id === nextWork.id);
  if (index >= 0) {
    works[index] = nextWork;
  } else {
    works.push(nextWork);
  }
  saveWorks();
  fillForm(nextWork);
  renderAll();
}

function editWork(id) {
  if (!isEditorPage) return;
  const work = works.find((item) => item.id === id);
  if (!work) return;
  editor.hidden = false;
  fillForm(work);
  editor.scrollIntoView({ behavior: 'smooth', block: 'start' });
}

function deleteSelectedWork() {
  if (!selectedWorkId) return;
  const work = works.find((item) => item.id === selectedWorkId);
  if (!work) return;
  const ok = confirm(`"${work.title}" 항목을 삭제할까요?`);
  if (!ok) return;
  works = works.filter((item) => item.id !== selectedWorkId);
  selectedWorkId = '';
  fillForm();
  saveWorks();
  renderAll();
}

function exportData() {
  const blob = new Blob([JSON.stringify(works, null, 2)], { type: 'application/json' });
  const url = URL.createObjectURL(blob);
  const link = document.createElement('a');
  link.href = url;
  link.download = 'portfolio-works.json';
  link.click();
  URL.revokeObjectURL(url);
}

async function importData(file) {
  if (!file) return;
  const text = await file.text();
  const imported = JSON.parse(text);
  if (!Array.isArray(imported)) {
    alert('작품 목록 JSON 형식이 아니에요.');
    return;
  }
  works = imported;
  saveWorks();
  fillForm();
  renderAll();
}

function resetData() {
  const ok = confirm('브라우저에 저장된 수정 내용을 지우고 기본 목록으로 되돌릴까요?');
  if (!ok) return;
  localStorage.removeItem(storageKey);
  works = structuredClone(defaultWorks);
  fillForm();
  renderAll();
}

function readFileAsDataUrl(file) {
  if (!file) return Promise.resolve('');
  return new Promise((resolve, reject) => {
    const reader = new FileReader();
    reader.onload = () => resolve(reader.result);
    reader.onerror = reject;
    reader.readAsDataURL(file);
  });
}

function makeId(title) {
  const slug = title
    .toLowerCase()
    .trim()
    .replace(/[^a-z0-9가-힣]+/g, '-')
    .replace(/^-+|-+$/g, '');
  return `${slug || 'work'}-${Date.now().toString(36)}`;
}

function escapeHtml(value) {
  return String(value ?? '')
    .replaceAll('&', '&amp;')
    .replaceAll('<', '&lt;')
    .replaceAll('>', '&gt;')
    .replaceAll('"', '&quot;')
    .replaceAll("'", '&#039;');
}

